package upcastingConcept;

public class implicitCasting {
   public static void main(String[] args) {
	

	byte a =20;
	byte b =30;
	
	float x1 =30.14f;
	float x2 =40.12f;
	
	
	int c = (int) a;
	short d = (short) b;
	
	double x = (double) x1;
	double y = (double) x2;	
	
	System.out.println(c);
	System.out.println(d);
	
	System.out.println(x);
	System.out.println(y);
	
	
	
   }	
}
